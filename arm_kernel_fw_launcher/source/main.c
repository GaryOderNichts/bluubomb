static const char sd_path[] = "/vol/sdcard";

int _main()
{
    kernel_flush_dcache(0x081200F0, 0x4001); // giving a size >= 0x4000 flushes all cache

    int level = kernel_disable_interrupts();

    unsigned int control_register = disable_mmu();

    /* Patch kernel_error_handler to BX LR immediately */
    *(int*) 0x08129A24 = 0xE12FFF1E;

    // recover memory we've overwritten
    kernel_memcpy((void*) 0x081298BC, repairData_set_fault_behavior, sizeof(repairData_set_fault_behavior));
    kernel_memcpy((void*) 0x081296E4, repairData_set_panic_behavior, sizeof(repairData_set_panic_behavior));
    kernel_memcpy((void*) 0x11f00558, repairData_pad_root_thread, sizeof(repairData_pad_root_thread));

    for (int i = 0; i < 32; i++) {
        if (i < 11) {
            ((char*)(0x050663B4 - 0x05000000 + 0x081C0000))[i] = sd_path[i];
        }
        else {
            ((char*)(0x050663B4 - 0x05000000 + 0x081C0000))[i] = '\0';
        }
    }

    *(int*)(0x050282AE - 0x05000000 + 0x081C0000) = 0xF031FB43; // bl launch_os_hook

    *(int*)(0x05052C44 - 0x05000000 + 0x081C0000) = 0xE3A00000; // mov r0, #0
    *(int*)(0x05052C48 - 0x05000000 + 0x081C0000) = 0xE12FFF1E; // bx lr

    *(int*)(0x0500A818 - 0x05000000 + 0x081C0000) = 0x20002000; // mov r0, #0; mov r0, #0

    *(int*)(0x040017E0 - 0x04000000 + 0x08280000) = 0xE3A00000;
    *(int*)(0x040019C4 - 0x04000000 + 0x08280000) = 0xE3A00000;
    *(int*)(0x04001BB0 - 0x04000000 + 0x08280000) = 0xE3A00000;
    *(int*)(0x04001D40 - 0x04000000 + 0x08280000) = 0xE3A00000;

    kernel_memcpy((void*) (0x05059938 - 0x05000000 + 0x081C0000), os_launch_hook, sizeof(os_launch_hook));

    *(int*) 0x1555500 = 0;

    /* REENABLE MMU */
    restore_mmu(control_register);

    kernel_invalidate_dcache(0x081298BC, 0x4001); // giving a size >= 0x4000 invalidates all cache
    kernel_invalidate_icache();

    kernel_enable_interrupts(level);

    // kernel_ios_shutdown(1);

    return 0;
}